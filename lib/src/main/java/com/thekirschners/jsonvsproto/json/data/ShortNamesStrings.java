package com.thekirschners.jsonvsproto.json.data;

public class ShortNamesStrings {
    String sn1;
    String sn2;
    String sn3;
    String sn4;
    String sn5;

    public ShortNamesStrings() {
    }

    public ShortNamesStrings(String sn1, String sn2, String sn3, String sn4, String sn5) {
        this.sn1 = sn1;
        this.sn2 = sn2;
        this.sn3 = sn3;
        this.sn4 = sn4;
        this.sn5 = sn5;
    }

    public String getSn1() {
        return sn1;
    }

    public void setSn1(String sn1) {
        this.sn1 = sn1;
    }

    public String getSn2() {
        return sn2;
    }

    public void setSn2(String sn2) {
        this.sn2 = sn2;
    }

    public String getSn3() {
        return sn3;
    }

    public void setSn3(String sn3) {
        this.sn3 = sn3;
    }

    public String getSn4() {
        return sn4;
    }

    public void setSn4(String sn4) {
        this.sn4 = sn4;
    }

    public String getSn5() {
        return sn5;
    }

    public void setSn5(String sn5) {
        this.sn5 = sn5;
    }
}
